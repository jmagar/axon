HANDLER_REQUIRED_FIELDS: dict[str, set[str]] = {
    "ask": {"query"},
    "endpoints": {"url"},
    "evaluate": {"query"},
    "map": {"url"},
    "purge": {"target"},
    "query": {"query"},
    "research": {"query"},
    "retrieve": {"url"},
    "scrape": {"url"},
    "screenshot": {"url"},
    "search": {"query"},
}

OPTIONAL_FIELD_OVERRIDES: dict[str, set[str]] = {
    # `prefix` is a non-Option bool in Rust but has `#[serde(default)]`; clients
    # may omit it and receive the default `false`.
    "purge": {"prefix"},
}


def _classify_actions(
    structs: dict[str, StructDef],
) -> tuple[list[str], list[str]]:
    """Split actions into subaction families and direct singleton actions."""
    lifecycle_actions: list[str] = []
    direct_actions: list[str] = []
    for struct_name, action in sorted(STRUCT_TO_ACTION.items(), key=lambda x: x[1]):
        sdef = structs.get(struct_name)
        if sdef and sdef.subaction_enum_name:
            lifecycle_actions.append(action)
        else:
            direct_actions.append(action)
    return lifecycle_actions, direct_actions


def _action_to_struct(action: str) -> str:
    """Convert action name back to struct name."""
    for struct_name, act in STRUCT_TO_ACTION.items():
        if act == action:
            return struct_name
    return ""


def _field_name_with_aliases(field: FieldDef) -> str:
    text = f"`{field.name}`"
    if field.aliases:
        aliases = ", ".join(f"`{alias}`" for alias in field.aliases)
        text += f" (aliases: {aliases})"
    return text


def _start_requirement_summary(action: str, sdef: StructDef) -> str:
    """Summarize what the 'start' subaction requires."""
    match action:
        case "crawl":
            return "start requires `urls` (array)"
        case "extract":
            return "start requires `urls` (array)"
        case "embed":
            return "start requires `input` (string)"
        case "ingest":
            return (
                "start requires `target`; `source_type` is optional and "
                "auto-detected from `target` (github/gitlab/gitea/git/reddit/"
                "youtube/rss) when omitted"
            )
        case _:
            req = sdef.required_fields()
            if req:
                return "requires " + ", ".join(f"`{f.name}`" for f in req)
            return "no required fields"
