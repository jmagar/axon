"""Markdown renderer for MCP schema documentation.

Takes parsed struct/enum definitions and produces the full
MCP-TOOL-SCHEMA.md content.
"""

from __future__ import annotations

from datetime import datetime, timezone

from mcp_schema_models import (
    CRAWL_FIELD_DESCRIPTIONS,
    MCP_AUTH_ENV_VARS,
    MCP_TRANSPORT_ENV_VARS,
    RUNTIME_ENV_VARS,
    RUNTIME_ENV_VARS_DEPRECATED,
    STRUCT_TO_ACTION,
    EnumDef,
    FieldDef,
    StructDef,
)


def generate_markdown(
    structs: dict[str, StructDef],
    enums: dict[str, EnumDef],
) -> str:
    """Produce the full MCP-TOOL-SCHEMA.md content."""
    today = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    lines: list[str] = []

    def emit(text: str = "") -> None:
        lines.append(text)

    _emit_header(emit, today)
    _emit_contract(emit)
    _emit_task_augmented_calls(emit)
    _emit_success_envelope(emit)
    _emit_parser_rules(emit)

    # Classify actions
    lifecycle_actions, direct_actions = _classify_actions(structs)

    _emit_preferred_client_actions(emit, lifecycle_actions, direct_actions)
    _emit_response_policy(emit)
    _emit_direct_actions(emit, direct_actions, structs)
    _emit_crawl_parameters(emit, structs)
    _emit_lifecycle_families(emit, lifecycle_actions, structs, enums)
    _emit_ingest_source_types(emit, enums)
    _emit_sessions_ingest_options(emit, structs)
    _emit_enum_values(emit, enums)
    _emit_pagination(emit)
    _emit_mcp_resources(emit)
    _emit_runtime_dependencies(emit)
    _emit_error_semantics(emit)

    return "\n".join(lines)


